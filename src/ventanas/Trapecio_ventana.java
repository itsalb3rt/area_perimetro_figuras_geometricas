    private void jButton_calcular_area_trapecioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton_calcular_area_trapecioMouseClicked
        // Calcular el perimetro del cuadrado
        // Calcular el area del cuadrado
        double resultado;
        Double ladoA = new Double(this.jTextField_ladoA.getText()); //Convirtiendo los datos de entrada a double       
        Double ladoB = new Double(this.jTextField_ladoB.getText()); //Convirtiendo los datos de entrada a double       
        Double altura = new Double(this.jTextField_altura_area.getText()); //Convirtiendo los datos de entrada a double       
        
        Trapecio trapecio = new Trapecio(); //Nuevo objeto de tipo cuadrado
        trapecio.setLadoA(ladoA); //Enviadno el valor a calcular al set 
        trapecio.setLadoB(ladoB); //Enviadno el valor a calcular al set
        trapecio.setAltura(altura); //Enviadno el valor a calcular al set
        resultado = trapecio.calcularArea(); //Almacenando resultaod del calculo en una variable resultado
        
        String salida = String.valueOf(resultado); //Pasando el resultaod a string para luego imprimirlo en ventana por un campo label jframe
        
        this.jLabel_resultado_area_trapecio.setText("Resultado: "+salida+" cm2"); //Enviando el resultado al label
    }//GEN-LAST:event_jButton_calcular_area_trapecioMouseClicked

